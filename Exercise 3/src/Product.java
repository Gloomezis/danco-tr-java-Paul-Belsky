
public class Product {
	private String name;
	private int weight;

	public Product(String name, int weigth) {

		this.name = name;
		this.weight = weigth;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getWeight() {
		return weight;
	}

	public void setWeight(int weigth) {
		this.weight = weigth;
	}

}
